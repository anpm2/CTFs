func = {
    '>': '0',
    '<': '1',
    '+': '2',
    '-': '3',
    '.': '4',
    ',': '5',
    '[': '6',
    ']': '7'
}
bf_code = "++++++++++[>+>+++>+++++++>++++++++++<<<<-]>>>+++++.++.------------.<++.>++.>----------------.<+++.<.++++++++++++++++++.--.++.+++."
inp = ''
for c in bf_code:
    if c in func:
        inp += func[c]
print(inp)